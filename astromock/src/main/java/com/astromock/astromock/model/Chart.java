package com.astromock.astromock.model;
public class Chart {

    private String sun;
    private String moon;
    private String mars;

    // Getter and Setter for sun
    public String getSun() {
        return sun;
    }

    public void setSun(String sun) {
        this.sun = sun;
    }

    // Getter and Setter for moon
    public String getMoon() {
        return moon;
    }

    public void setMoon(String moon) {
        this.moon = moon;
    }

    // Getter and Setter for mars
    public String getMars() {
        return mars;
    }

    public void setMars(String mars) {
        this.mars = mars;
    }
}
